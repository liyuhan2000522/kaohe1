package hengzhi.entity;

public class SortInfo {

    //类别id
    private int id;

    //类别名称
    private String sortName;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSortName() {
        return sortName;
    }

    public void setSortName(String sortName) {
        this.sortName = sortName;
    }
}
